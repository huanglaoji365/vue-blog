<template>
  <div id="app">
    <router-view />
  </div>
</template>

<script setup>
import { onMounted } from 'vue'
import { useAuthStore } from '@/stores/auth'

// 根组件
const authStore = useAuthStore()

onMounted(async () => {
  // 应用启动时，如果有 token，则获取用户信息
  if (authStore.token) {
    await authStore.fetchProfile()
  }
})
</script>

